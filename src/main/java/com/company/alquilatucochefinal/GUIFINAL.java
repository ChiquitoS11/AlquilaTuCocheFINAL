    /**
     * Los HashSet eliminan automaticamente los elementos repetidos
     * <P>
     * este metodo borra todos los elementos repetidos
     * <P>
     * Dejando su aparicion una unica vez por elemento.
     *
     * @param comboBox
     */
    public void eliminarElementosDuplicados(JComboBox<String> comboBox) {
        HashSet<String> elementosUnicos = new HashSet<>();

        for (int i = 0; i < comboBox.getItemCount(); i++) {
            elementosUnicos.add(comboBox.getItemAt(i));
        }
        comboBox.removeAllItems();

        elementosUnicos.forEach(comboBox::addItem);
    }

