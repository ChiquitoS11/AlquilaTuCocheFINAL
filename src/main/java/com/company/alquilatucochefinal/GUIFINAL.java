    private void jLabel3MouseWheelMoved(java.awt.event.MouseWheelEvent evt) {//GEN-FIRST:event_jLabel3MouseWheelMoved
        
        JViewport viewport = jScrollPane3.getViewport();
        Rectangle viewRect = viewport.getViewRect();
                
        int scrollAmount = evt.getUnitsToScroll() * 20; // cambiar velocidad del scroll
 
        // sepaCristo...
        viewRect.y += scrollAmount;
        viewRect.y = Math.max(0, Math.min(viewRect.y, viewport.getViewSize().height - viewRect.height));
        viewport.setViewPosition(viewRect.getLocation());

    }//GEN-LAST:event_jLabel3MouseWheelMoved

