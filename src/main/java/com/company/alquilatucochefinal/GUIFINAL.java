    /**
     * carga la fuente para su posterior uso en cada componente
     *
     * @param fontSize tamanio en int de la fuente
     * @return
     */
    public Font cargarFuente(int fontSize) {
        try {
            // Cargar la fuente desde el archivo externo
            return Font.createFont(Font.TRUETYPE_FONT, new File(rutaFuente)).deriveFont(Font.PLAIN, fontSize);
        } catch (Exception e) {
            e.printStackTrace();
            return null;
        }
    }

